    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(txtID.getText());
            String stdName = txtName.getText();
            String stdBlood = txtBlood.getText();
            String stdFather = txtFName.getText();
            String stdCity = txtCity.getText();
            String stdPhone = txtPhone.getText();
            int stdClass = Integer.parseInt(txtClass.getText());

            String sql = "UPDATE student SET stdName='" + stdName + "', stdFatherName='" + stdFather + 
                     "', stdBlood='" + stdBlood + "', stdPhone='" + stdPhone + "', stdCity='" + 
                     stdCity + "', class=" + stdClass + " WHERE stdID=" + stdId;

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data is successfully inserted");

            txtName.setText("");
            txtFName.setText("");
            txtCity.setText("");
            txtPhone.setText("");
            txtClass.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

