    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(txtID.getText());
            String sql = "SELECT * FROM student WHERE stdID='"+stdId+"'";
            rs = stmt.executeQuery(sql);
            if(rs.next()){
                txtName.setText(rs.getString("stdName"));
                txtFName.setText(rs.getString("stdFatherName"));
                txtCity.setText(rs.getString("stdCity"));
                txtBlood.setText(rs.getString("stdBlood"));
                txtPhone.setText(rs.getString("stdPhone"));
                txtClass.setText(String.format("%s", rs.getInt("class")));
            }
            else{
                JOptionPane.showMessageDialog(null, "Record Not Found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

